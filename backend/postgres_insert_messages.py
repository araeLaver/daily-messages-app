def insert_messages_to_postgres():
    """PostgreSQL에 700개 메시지 데이터 삽입"""
    
    try:
        # PostgreSQL 연결
        conn = get_db_connection()
        cursor = conn.cursor()
        
        # 테이블이 존재하지 않으면 생성
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS morning_dev (
                id SERIAL PRIMARY KEY,
                text TEXT NOT NULL,
                author VARCHAR(255) DEFAULT '',
                category VARCHAR(100) NOT NULL,
                source VARCHAR(100) DEFAULT 'custom',
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # 기존 데이터 삭제
        cursor.execute("DELETE FROM morning_dev")
        print("기존 데이터 삭제 완료")
        
        # 새로운 메시지 데이터 삽입
        total_inserted = 0
        
        for category, messages in CATEGORIES_MESSAGES.items():
            for message_text in messages:
                # 데이터 삽입
                cursor.execute("""
                    INSERT INTO morning_dev (text, author, category, source, created_at) 
                    VALUES (%s, %s, %s, %s, %s)
                """, (
                    message_text,
                    "",  # 작성자 빈 문자열 (하이픈 제거를 위해)
                    category,
                    "system",
                    datetime.now()
                ))
                
                total_inserted += 1
        
        # 변경사항 저장
        conn.commit()
        print(f"✅ 총 {total_inserted}개의 메시지가 PostgreSQL에 추가되었습니다!")
        
        # 카테고리별 통계 출력
        print("\n📊 카테고리별 메시지 수:")
        for category in CATEGORIES_MESSAGES.keys():
            cursor.execute("SELECT COUNT(*) FROM morning_dev WHERE category = %s", (category,))
            count = cursor.fetchone()[0]
            print(f"  {category}: {count}개")
        
        # 전체 통계
        cursor.execute("SELECT COUNT(*) FROM morning_dev")
        total_count = cursor.fetchone()[0]
        print(f"\n📈 전체 메시지 수: {total_count}개")
        
        cursor.close()
        conn.close()
        
    except Exception as e:
        print(f"❌ 오류 발생: {str(e)}")
        if 'conn' in locals():
            conn.rollback()
            conn.close()

if __name__ == "__main__":
    print("PostgreSQL에 메시지 데이터 삽입을 시작합니다...")
